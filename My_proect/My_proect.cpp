#include <iostream>
#include <cstdlib>

using namespace std;

int main()
{
    //Изменение кодировки консоли:
    system("chcp 1251>nul");
    //Верхняя граница суммы, значение суммы и индексная переменная:
    int n = 10, s = 0, k = 1;
    //Опреаторы цикла для вычисления суммы:
    while (k <= n) {
        //Добавление нового слогаемого к сумме:
        s = s + k * k;
        //Увеличение (на 1) значения индексной переменной:
        k++;
    }
    //Отображение результата вычисления:
    cout << "Сумма квадратов от 1 до " << n << ": " << s << endl;
    //Задержка консольного окна:
    system("pause>nul");
    return 0;
}
